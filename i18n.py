def t(key: str, *args) -> str:
    import streamlit as st
    lang = st.session_state.get("lang", "zh")
    text = TRANSLATIONS.get(lang, TRANSLATIONS["zh"]).get(key, key)
    if args:
        return text.format(*args)
    return text


HEADER_KEYS = [
    "header_deploy", "header_theme", "header_rerun",
    "header_auto_rerun", "header_clear_cache",
    "header_print", "header_record",
    "header_settings", "header_main_menu",
]


def get_header_tooltips(lang: str) -> dict:
    lang = lang or "zh"
    trans = TRANSLATIONS.get(lang, TRANSLATIONS["zh"])
    return {k: trans.get(k, k) for k in HEADER_KEYS}
